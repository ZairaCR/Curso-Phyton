#definir una variable
a = 5
b = 10
c = a + b
print(c)

numero = 10
numero += 8
print(numero)

#definir una variable con camelCase
nombreDeLaQueEstaAprendiendo = "Paola"

#definir una variable con snake_case
nombre_de_la_gatita = "Zaira"
print(nombre_de_la_gatita)

#concatenar con +
bienvenida = "Hola " + nombre_de_la_gatita + " como estas?"
print(bienvenida)

#concatenar con f-string
bienvenida = f"Hola {nombreDeLaQueEstaAprendiendo} como estas?"
print(bienvenida)

#operadores de pertenencia (in / not in) (esta / no esta)
print("estas" in bienvenida)
print("como" not in bienvenida)

#del sirve para eliminar 
del bienvenida

